function curatedAnswer(question: string, paper: Paper): string | null {
  if (paper.slug !== "synthetic-respondents") return null;
  const qTokens = tokenize(question);
  for (const rule of syntheticRules) {
    const hit = rule.keywords.some((k) => {
      const kt = tokenize(k);
      // multi-word keyword: all its tokens must appear; single word: prefix match
      // kt.length guard: a keyword that tokenizes to nothing (e.g. the
      // 2-letter "ai") must not vacuously match every question.
      return kt.length > 0 && kt.every((t) => qTokens.some((qt) => tokenMatch(qt, t)));
    });
    if (hit) return rule.answer;
  }
  return null;
}

// ——— public API ———

export function getMockAnswer(question: string, paper: Paper, selection?: string): string {
  const best = bestSentence(question, paper);

  // A strong sentence match means the fact is written in the paper — answer it.
  let answer: string;
  if (best.score >= 2) {
    answer = best.text;
  } else {
    const curated = curatedAnswer(question, paper);
    if (curated) {
      answer = curated;
    } else if (best.score >= 1) {
      // On-topic but no precise sentence — give the paper's abstract.
      answer = paper.abstract;
    } else {
      return OUT_OF_SCOPE; // genuinely nothing in the paper matches
    }
  }

  // Acknowledge a highlighted passage on the exchange it was attached to.
  return selection ? `Looking at the passage you highlighted — ${answer}` : answer;
}

