//   - Paper in scope: answer is retrieved from the paper's own text, so any
//     fact actually written in the paper (e.g. "40 question variants") gets
//     answered, for every paper — not just the one with hand-written rules.
//   - No paper: a small set of scripted portal answers.
// When ANTHROPIC_API_KEY is set, /api/chat skips this file entirely.

import type { Paper } from "./content";

// Exact demo line for anything we can't ground in real content.
const OUT_OF_SCOPE = "I have no verified Esomar work on that yet.";

// ——— tiny keyword retrieval over the paper text ———

const STOP = new Set(
  ("the a an and or of to in on for with is are was were be been being this that " +
    "these those it its as at by from we our you your i they them their he she his " +
    "her which who what when where why how do does did can could should would will " +
    "more than not no about into over across per any some all most each also has have " +
    "had but so if then there here me my")
    .split(" ")
);

function tokenize(s: string): string[] {
  return s
    .toLowerCase()
    .replace(/[^a-z0-9\s]/g, " ")
    .split(/\s+/)
    .filter((w) => w.length >= 3 && !STOP.has(w));
}

// Prefix match so plurals/inflections still hit: variants↔variant,
// limitations↔limit, wording↔word.
function tokenMatch(a: string, b: string): boolean {
  if (a === b) return true;
  const min = Math.min(a.length, b.length);
  return min >= 4 && (a.startsWith(b) || b.startsWith(a));
}

function splitSentences(text: string): string[] {
  return text
    .split(/(?<=[.?!])\s+(?=[A-Z0-9"'])/)
    .map((s) => s.trim())
    .filter((s) => s.length > 20);
}

function paperSentences(paper: Paper): string[] {
  return [
    ...splitSentences(paper.abstract),
    ...paper.sections.flatMap((sec) => sec.paragraphs.flatMap(splitSentences)),
  ];
}

// Returns the best-matching sentence and its score for a question.
function bestSentence(question: string, paper: Paper): { text: string; score: number } {
  const qTokens = tokenize(question);
  const wantsNumber = /\bhow many\b|\bhow much\b|\bnumber\b|\bcount\b|percent|%|\bhow long\b/.test(
    question.toLowerCase()
  );
  let best = { text: "", score: 0 };
  for (const sentence of paperSentences(paper)) {
    const sTokens = tokenize(sentence);
    let score = 0;
    for (const qt of qTokens) {
      if (sTokens.some((st) => tokenMatch(qt, st))) score += 1;
    }
    // Reward a number when the question asks for a quantity
    if (wantsNumber && /\d/.test(sentence)) score += 1.5;
    if (score > best.score) best = { text: sentence, score };
  }
  return best;
}

